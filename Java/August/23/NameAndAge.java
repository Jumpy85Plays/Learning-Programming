public class NameAndAge {
    public static void main(String[] args) {
        // name holder
        String name = "Gabriel Aguilar";
        int age = 20; // Age holder

        // Calculate age next year
        int ageNextYear = age + 1;

        // Prints your name and age next year
        System.out.println("Name next year: " + name);
        System.out.println("Age next year: " + ageNextYear);
    }
}
